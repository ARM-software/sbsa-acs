.text
.align 3

GCC_ASM_EXPORT(DataCacheCleanInvalidateVA)
GCC_ASM_EXPORT(DataCacheInvalidateVA)
GCC_ASM_EXPORT(DataCacheCleanVA)

ASM_PFX(DataCacheCleanInvalidateVA):
  dc  civac, x0
  dsb sy
  isb
  ret

ASM_PFX(DataCacheCleanVA):
  dc  cvac, x0
  dsb ish
  isb
  ret

ASM_PFX(DataCacheInvalidateVA):
  dc  ivac, x0
  dsb ish
  isb
  ret
